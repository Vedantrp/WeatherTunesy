export default async function handler(req, res) {
  try {
    if (req.method !== "POST") return res.status(405).json({ error: "POST only" });

    const { token, tracks, mood = "Weather Mix" } = req.body || {};
    if (!token) return res.status(401).json({ error: "Missing token" });
    if (!tracks?.length) return res.status(400).json({ error: "No tracks provided" });

    // 1ï¸âƒ£ Create playlist
    const me = await fetch("https://api.spotify.com/v1/me", {
      headers: { Authorization: `Bearer ${token}` }
    }).then(r => r.json());

    const playlistRes = await fetch(`https://api.spotify.com/v1/users/${me.id}/playlists`, {
      method: "POST",
      headers: { "Content-Type": "application/json", Authorization: `Bearer ${token}` },
      body: JSON.stringify({
        name: `WeatherTunes â€“ ${mood} ðŸŒ¤ï¸`,
        description: "Generated by WeatherTunes",
        public: false
      })
    });

    const playlist = await playlistRes.json();
    if (!playlist?.id) return res.status(500).json({ error: "Playlist create failed" });

    // 2ï¸âƒ£ Add tracks
    const uris = tracks.map(t => t.uri).filter(Boolean);
    await fetch(`https://api.spotify.com/v1/playlists/${playlist.id}/tracks`, {
      method: "POST",
      headers: { "Content-Type": "application/json", Authorization: `Bearer ${token}` },
      body: JSON.stringify({ uris })
    });

    res.status(200).json({
      playlistUrl: playlist.external_urls.spotify
    });
  } catch (err) {
    console.error("Playlist error", err);
    res.status(500).json({ error: "create fail" });
  }
}
